# 🕌 Старт
def start(update, context):
    update.message.reply_text(
        "Ассаляму алейкум!\n\n"
        "Добро пожаловать в HIKMAH HUB — исламскую энциклопедию.\n"
        "Выбери тему или задай вопрос.",
        reply_markup=main_menu
    )


# 🔧 Обработчик кнопок
def buttons(update, context):
    text = update.message.text.strip()
    lower = text.lower()
    cap = text.capitalize()

    # Главное меню
    if lower == "исламские темы":
        update.message.reply_text("Выбери тему:", reply_markup=islam_topics)
        return

    if lower == "аят":
        update.message.reply_text("Напиши номер аята (например: 2:255).")
        return

    if lower == "хадис":
        update.message.reply_text("Напиши ключевое слово хадиса.")
        return

    if lower == "задать вопрос":
        update.message.reply_text("Напиши свой исламский вопрос.")
        return

    if lower == "помощь":
        update.message.reply_text(
            "Команды:\n"
            "/start — меню\n"
            "Исламские темы — список разделов\n"
            "Аят — объяснение аята\n"
            "Хадис — объяснение хадиса\n"
            "Задать вопрос — любой исламский вопрос\n\n"
            "Бот отвечает только по исламским темам."
        )
        return

    # Главная тема → показать подкатегории
    if cap in subtopics:
        update.message.reply_text(
            f"Выбери подкатегорию темы «{cap}»:",
            reply_markup=ReplyKeyboardMarkup([subtopics[cap]], resize_keyboard=True)
        )
        return

    # 50 тем → статья
    if cap in islam_topics.keyboard[0] or any(cap in row for row in islam_topics.keyboard):
        answer = ask_groq(f"Энциклопедическая статья по теме: {cap}.")
        update.message.reply_text(answer)
        return

    # Подкатегория → статья
    for main_topic, subs in subtopics.items():
        if text in subs:
            answer = ask_groq(
                f"Энциклопедическая статья. Главная тема: {main_topic}. Подтема: {text}."
            )
            update.message.reply_text(answer)
            return

    # Аят (например: 2:255)
    if ":" in text and all(part.isdigit() for part in text.split(":", 1)):
        answer = ask_groq(
            f"Энциклопедическая статья по аяту Корана: {text}."
        )
        update.message.reply_text(answer)
        return

    # Любой исламский вопрос
    answer = ask_groq(
        f"Энциклопедическая статья по исламскому вопросу: {text}."
    )
    update.message.reply_text(answer)


# 🚀 Запуск
def main():
    if not TELEGRAM_TOKEN or not GROQ_API_KEY:
        print("Ошибка: нет TELEGRAM_TOKEN или GROQ_API_KEY.")
        return

    updater = Updater(TELEGRAM_TOKEN, use_context=True)
    dp = updater.dispatcher

    dp.add_handler(CommandHandler("start", start))
    dp.add_handler(MessageHandler(Filters.text & ~Filters.command, buttons))

    updater.start_polling()
    updater.idle()


if __name__ == "__main__":
    main()
