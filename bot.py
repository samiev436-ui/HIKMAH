# 🧠 Запрос к Groq — формат «Исламская Энциклопедия»
def ask_groq(prompt: str) -> str:
    url = "https://api.groq.com/openai/v1/chat/completions"
    headers = {
        "Authorization": f"Bearer {GROQ_API_KEY}",
        "Content-Type": "application/json",
    }
    data = {
        "model": "llama-3.1-8b-instant",
        "messages": [
            {
                "role": "system",
                "content": (
                    "Ты — исламская энциклопедия HIKMAH HUB. Отвечай строго по Исламу.\n\n"
                    "Формат каждой статьи:\n"
                    "📘 Название раздела\n"
                    "📚 Определение (1–3 предложения)\n"
                    "📖 Аят (один, с кратким смыслом)\n"
                    "🕌 Хадис (один, кратко)\n"
                    "💬 Толкование (как в книге, 3–6 предложений)\n"
                    "📜 История или пример\n"
                    "🧭 Практическое применение (что делать мусульманину)\n\n"
                    "Правила:\n"
                    "- только исламские темы;\n"
                    "- без политики, психологии, философии, бизнеса, отношений;\n"
                    "- не давай фетвы;\n"
                    "- уважительный тон;\n"
                    "- стиль как в исламской книге."
                )
            },
            {
                "role": "user",
                "content": prompt
            }
        ],
        "temperature": 0.4,
    }

    try:
        resp = requests.post(url, json=data, headers=headers, timeout=30)
        resp.raise_for_status()
        return resp.json()["choices"][0]["message"]["content"]
    except Exception as e:
        logging.error(f"Groq error: {e}")
        return "Ошибка при обращении к энциклопедии. Попробуй ещё раз."


